package com.aksi.domain.user.entity;

/**
 * Перелік ролей користувачів у системі.
 */
public enum RoleEntity {
    ADMIN,       // Адміністратор (повний доступ до всієї системи)
    MANAGER,     // Менеджер (може керувати клієнтами, замовленнями та обмежено персоналом)
    STAFF        // Персонал (базовий доступ: робота з клієнтами, замовленнями)
}
