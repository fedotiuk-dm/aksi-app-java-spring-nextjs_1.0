package com.aksi.domain.order.statemachine.stage4.enums;

/**
 * Стани Stage4 - Підтвердження та завершення замовлення.
 *
 * Етап 4 включає:
 * - 4.1. Перегляд замовлення з детальним розрахунком
 * - 4.2. Юридичні аспекти (підпис та згода з умовами)
 * - 4.3. Формування та друк квитанції
 * - 4.4. Завершення процесу
 */
public enum Stage4State {

    /**
     * Початковий стан Stage4 - очікування початку процесу підтвердження.
     */
    STAGE4_INITIALIZED,

    /**
     * Перегляд детального підсумку замовлення.
     * Відображення повної інформації про замовлення з розрахунками.
     */
    ORDER_SUMMARY_REVIEW,

    /**
     * Юридичне оформлення - прийняття умов та цифровий підпис.
     * Підтвердження умов надання послуг та збереження підпису клієнта.
     */
    LEGAL_ACCEPTANCE,

    /**
     * Юридичне оформлення завершено.
     * Клієнт прийняв умови та поставив підпис.
     */
    LEGAL_ACCEPTANCE_COMPLETED,

    /**
     * Генерація квитанції.
     * Створення PDF та/або друкованої версії квитанції.
     */
    RECEIPT_GENERATION,

    /**
     * Квитанція згенерована.
     * PDF документ створено та готовий для друку або відправки.
     */
    RECEIPT_GENERATED,

    /**
     * Завершення замовлення.
     * Фінальне збереження та завершення процесу Order Wizard.
     */
    ORDER_COMPLETION,

    /**
     * Stage4 успішно завершено.
     */
    STAGE4_COMPLETED,

    /**
     * Помилка під час Stage4.
     */
    STAGE4_ERROR
}
