package com.aksi.domain.order.statemachine;

/**
 * Події (Events) для Order Wizard State Machine
 *
 * Кожна подія представляє дію користувача або системи,
 * що може викликати перехід між станами
 */
public enum OrderEvent {

    // === ПОЧАТКОВІ ПОДІЇ ===
    START_ORDER,             // Початок створення нового замовлення
    CANCEL_ORDER,            // Скасування замовлення на будь-якому етапі

    // === ЕТАП 1: Навігаційні події ===
    CLIENT_SELECTED,         // Клієнт вибраний або створений
    ORDER_INFO_COMPLETED,    // Базова інформація замовлення заповнена

    // === ЕТАП 2: Менеджер предметів ===
    ADD_ITEM,                // Кнопка "Додати предмет" - запуск підвізарда
    START_ITEM_WIZARD,       // Початок підвізарда предмета
    ITEM_ADDED,              // Предмет додано до замовлення
    EDIT_ITEM,               // Редагування існуючого предмета
    DELETE_ITEM,             // Видалення предмета з замовлення
    CANCEL_ITEM_WIZARD,      // Скасування підвізарда предмета
    ITEMS_COMPLETED,         // Всі предмети додані, перехід до наступного етапу

    // === ЕТАП 3: Загальні параметри ===
    EXECUTION_PARAMS_SET,    // Параметри виконання встановлені
    DISCOUNTS_APPLIED,       // Знижки застосовані
    PAYMENT_PROCESSED,       // Оплата оброблена
    ADDITIONAL_INFO_COMPLETED, // Додаткова інформація заповнена

    // === ЕТАП 4: Підтвердження ===
    REVIEW_ORDER,            // Перехід до перегляду замовлення
    ORDER_APPROVED,          // Замовлення схвалено для завершення
    TERMS_ACCEPTED,          // Умови прийняті, підпис поставлений
    RECEIPT_GENERATED,       // Квитанція згенерована та надрукована

    // === НАВІГАЦІЙНІ ПОДІЇ ===
    GO_BACK,                 // Повернення на попередній крок
    GO_FORWARD,              // Перехід на наступний крок
    JUMP_TO_STEP,            // Перехід до конкретного кроку

    // === СИСТЕМНІ ПОДІЇ ===
    AUTO_SAVE,               // Автоматичне збереження
    VALIDATION_ERROR,        // Помилка валідації
    SYSTEM_ERROR,            // Системна помилка
    TIMEOUT,                 // Таймаут сесії

    // === СПЕЦІАЛЬНІ ПОДІЇ ДЛЯ ПІДВІЗАРДА ===
    RESTART_ITEM_WIZARD,     // Перезапуск підвізарда предмета
    SKIP_PHOTOS,             // Пропуск фотодокументації
    RECALCULATE_PRICE,       // Перерахунок ціни при зміні параметрів

    // === ПОДІЇ ДЛЯ ВАЛІДАЦІЇ ===
    VALIDATE_STEP,           // Валідація поточного кроку
    FORCE_NEXT,              // Примусовий перехід (з попередженням)

    // === ПОДІЇ ДЛЯ ЗБЕРЕЖЕННЯ ===
    SAVE_DRAFT,              // Збереження чернетки
    LOAD_DRAFT,              // Завантаження чернетки
    CLEAR_DRAFT              // Очищення чернетки
}
