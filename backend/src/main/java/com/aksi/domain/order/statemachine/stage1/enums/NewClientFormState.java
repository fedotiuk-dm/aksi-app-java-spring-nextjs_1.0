package com.aksi.domain.order.statemachine.stage1.enums;

/**
 * Enum для станів форми нового клієнта.
 * Визначає всі можливі стани процесу створення клієнта.
 */
public enum NewClientFormState {

    /**
     * Початковий стан форми.
     */
    INIT,

    /**
     * Стан заповнення базових даних клієнта.
     */
    FILLING_BASIC_INFO,

    /**
     * Стан заповнення контактної інформації.
     */
    FILLING_CONTACT_INFO,

    /**
     * Стан валідації введених даних.
     */
    VALIDATING,

    /**
     * Стан перевірки дублікатів.
     */
    CHECKING_DUPLICATES,

    /**
     * Знайдені дублікати, очікується рішення користувача.
     */
    DUPLICATES_FOUND,

    /**
     * Стан збереження клієнта.
     */
    SAVING,

    /**
     * Клієнт успішно створений.
     */
    COMPLETED,

    /**
     * Помилка у процесі створення.
     */
    ERROR,

    /**
     * Процес скасовано.
     */
    CANCELLED
}
