package com.aksi.domain.order.entity;

/**
 * Enum representing the types of stains that can be documented on a clothing item.
 */
public enum StainType {
    /**
     * Grease stains.
     */
    GREASE,
    
    /**
     * Blood stains.
     */
    BLOOD,
    
    /**
     * Protein-based stains.
     */
    PROTEIN,
    
    /**
     * Wine stains.
     */
    WINE,
    
    /**
     * Coffee stains.
     */
    COFFEE,
    
    /**
     * Grass stains.
     */
    GRASS,
    
    /**
     * Ink stains.
     */
    INK,
    
    /**
     * Cosmetics-based stains.
     */
    COSMETICS,
    
    /**
     * Other types of stains not categorized above.
     */
    OTHER
}
