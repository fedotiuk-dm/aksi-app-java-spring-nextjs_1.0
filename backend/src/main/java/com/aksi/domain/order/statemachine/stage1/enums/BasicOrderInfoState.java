package com.aksi.domain.order.statemachine.stage1.enums;

/**
 * Стани процесу заповнення базової інформації замовлення в етапі 1.3.
 * Представляє різні фази створення базової інформації про замовлення.
 */
public enum BasicOrderInfoState {
    /**
     * Початковий стан - жодної інформації не введено.
     */
    INIT,

    /**
     * Генерація номера квитанції в процесі.
     */
    GENERATING_RECEIPT_NUMBER,

    /**
     * Номер квитанції згенеровано.
     */
    RECEIPT_NUMBER_GENERATED,

    /**
     * Введення унікальної мітки.
     */
    ENTERING_UNIQUE_TAG,

    /**
     * Унікальна мітка введена та перевірена.
     */
    UNIQUE_TAG_ENTERED,

    /**
     * Вибір філії для прийому замовлення.
     */
    SELECTING_BRANCH,

    /**
     * Філію обрано.
     */
    BRANCH_SELECTED,

    /**
     * Встановлення дати створення замовлення.
     */
    SETTING_CREATION_DATE,

    /**
     * Дата створення встановлена.
     */
    CREATION_DATE_SET,

    /**
     * Базову інформацію замовлення заповнено повністю.
     */
    COMPLETED,

    /**
     * Помилка в процесі заповнення базової інформації.
     */
    ERROR
}
