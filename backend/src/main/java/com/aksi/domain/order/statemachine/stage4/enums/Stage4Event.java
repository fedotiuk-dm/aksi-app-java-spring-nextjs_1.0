package com.aksi.domain.order.statemachine.stage4.enums;

/**
 * Події Stage4 - Підтвердження та завершення замовлення.
 *
 * События для управління переходами між станами Stage4.
 */
public enum Stage4Event {

    /**
     * Ініціалізація Stage4.
     * Запускає процес підтвердження замовлення.
     */
    INITIALIZE_STAGE4,

    /**
     * Перегляд підсумку замовлення.
     * Відображення детального підсумку для перевірки клієнтом.
     */
    REVIEW_ORDER_SUMMARY,

    /**
     * Прийняття юридичних умов.
     * Клієнт підтверджує згоду з умовами та ставить підпис.
     */
    ACCEPT_LEGAL_TERMS,

    /**
     * Генерація квитанції.
     * Створення PDF документа квитанції.
     */
    GENERATE_RECEIPT,

    /**
     * Завершення замовлення.
     * Фінальне збереження та завершення Order Wizard.
     */
    COMPLETE_ORDER,

    /**
     * Помилка під час обробки.
     * Перехід до стану помилки при будь-якій невдачі.
     */
    ERROR_OCCURRED,

    /**
     * Повернення до попереднього кроку.
     * Дозволяє повернутися для внесення змін.
     */
    GO_BACK
}
