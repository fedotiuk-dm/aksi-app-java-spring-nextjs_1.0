package com.aksi.domain.order.statemachine.stage2.enums;

/**
 * Стани головного екрану менеджера предметів (Етап 2.0).
 */
public enum Stage2State {
    /**
     * Етап не розпочато
     */
    NOT_STARTED,

    /**
     * Ініціалізація головного екрану менеджера предметів
     */
    INITIALIZING,

    /**
     * Головний екран менеджера предметів (показує список доданих предметів)
     */
    ITEMS_MANAGER_SCREEN,

    /**
     * Активний підвізард додавання/редагування предмета
     */
    ITEM_WIZARD_ACTIVE,

    /**
     * Готовий до переходу на наступний етап
     */
    READY_TO_PROCEED,

    /**
     * Етап завершено успішно
     */
    COMPLETED,

    /**
     * Помилка в процесі виконання
     */
    ERROR
}
